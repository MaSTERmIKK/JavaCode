public class Cicli {
    public static void main(String[] args) {
        //While
        int i = 0;//inizializzazione della variabile di controllo;
        while (i<=5) {
            System.out.println("Valore di i: "+i);
            ++i;// incremento della variabile di controllo;
        }

        //Do-While
        int x = 0;
        do {
            System.out.println("Valore di x: " + x);
            ++x;// incremento della variabile di controllo;
        } while (x<=5);
    }

}



